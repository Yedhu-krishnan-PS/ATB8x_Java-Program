package Sept.ex_270924_conditionLoops;

/*The **ternary operator** is a concise way to perform conditional evaluations in programming,
 often serving as a shorthand for if-else statements.
condition ? expression_if_true : expression_if_false*/

public class Lab_018 {
    public static void main(String[] args) {
        int a = (25>36) ? 7: 8;
        System.out.println(a);
    }
}
/* If the condition is true first value(7) will be given as output,
 and if it is false then it will be the second value(8)
 */