package Sept.ex_270924_conditionLoops;

public class Lab_021 {
    public static void main(String[] args) {
        // Max, Min between two numbers.
        int tushar_salary = 4;
        int prabhu_salary = 7;
        // max
        // min
        int max = tushar_salary > prabhu_salary ? tushar_salary : prabhu_salary;
        int nothing =  tushar_salary < prabhu_salary ? tushar_salary : prabhu_salary;
        int min =  tushar_salary < prabhu_salary ? tushar_salary : prabhu_salary;

        System.out.println(max);
        System.out.println(nothing);
        System.out.println(min);

    }
}
